# 列表的遍历
list=['Google','Android',123]
for li in list:
    print(li)
print('===============')
#　元组的遍历
tu=('Google','Android',123)
for t in tu:
    print(t)
print('===============')
# 字典遍历
dict={'Google':'Tech','Tesla':'Car','OnePlus':'SamrtPhone'}
for dic in dict:
    print(dic)
print('===============')
# 集合的遍历
set = set(['Google','Facebook','Tesla','OnePlus'])
for par in set:
    print(par)

